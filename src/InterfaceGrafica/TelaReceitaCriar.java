    private void bt_cirarReceitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cirarReceitaActionPerformed
        
        try {
            
            if (degustacoes.isEmpty())
                throw new Exception("Deve have no mínimo 1 degustação da receita");
            
            
            
            int codigoReceita = Integer.parseInt(
                    txt_codigoReceita.getText()
            );
            
            String nomeReceita = txt_nomeReceita.getText();
            String nomeCozinheiro = txt_nomeCozinheiro.getText();
            Date dataCriacao = null;
            
            SimpleDateFormat dataFormato = new SimpleDateFormat(
                    "dd/MM/yyyy"
            );
            
            dataCriacao = dataFormato.parse(txt_dataCriacao.getText());
            
            String cpfCozinheiro = txt_cpfCozinheiro.getText();
            
            int codigoCategoria = Integer.parseInt(
                    txt_codigoCategoria.getText()
            );
            
            
            Receita receita = new Receita(
                    codigoReceita,
                    nomeReceita,
                    dataCriacao,
                    nomeCozinheiro,
                    cpfCozinheiro,
                    codigoCategoria
            );
            
            BancoDeDados banco = BancoDeDados.getInstance();
            
            banco.inserirReceita(receita, ingredientes);
            banco.inserirDegustacoes(degustacoes, codigoReceita);
            
            JOptionPane.showMessageDialog(
                    rootPane, 
                    "Receita Cadastrada com sucesso!",
                    "SUCESSO",
                    JOptionPane.INFORMATION_MESSAGE
            );
            
            
        } catch(Exception e) {
            
            JOptionPane.showMessageDialog(
                    rootPane,
                    e.getMessage(),
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_bt_cirarReceitaActionPerformed

