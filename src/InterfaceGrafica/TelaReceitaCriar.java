    public void mostrarDegustacoes() {
        
        DefaultTableModel model = (DefaultTableModel) 
                                   jTableDegustadores.getModel();
        
        SimpleDateFormat dataFormato = new SimpleDateFormat(
                    "dd/MM/yyyy"
        );
            
        
        
        while (model.getRowCount()> 0)
            model.removeRow(0);
        
        for (var degustacao : degustacoes) {
                
            String dataTeste = dataFormato.format(degustacao.get(1));
            
            model.addRow(new Object[]{
                    degustacao.get(0),
                    dataTeste,
                    degustacao.get(2)
                });
        }
        
    }
    
    public void mostrarIngredientes() {
        
        DefaultTableModel model = (DefaultTableModel) 
                                   jTable_consulta.getModel();
        
        while (model.getRowCount()> 0)
            model.removeRow(0);
        
        for (IngredienteReceita ingrediente : ingredientes) {
                model.addRow(new Object[]{
                    ingrediente.getCodigoIngrediente(),
                    ingrediente.getQuantidade(),
                    ingrediente.getMedida()
                });
        }
    }
    
