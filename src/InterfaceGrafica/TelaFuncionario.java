    private void bt_procurarCPFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_procurarCPFActionPerformed
        try {
            
            String cpf = JOptionPane.showInputDialog(
                    this,
                    "Digite o CPF do funcionário:"
            );
            
            if (cpf == null || cpf.equals(""))
                return;
            
            BancoDeDados banco = BancoDeDados.getInstance(
                    usuario,
                    senha,
          nomedb
            );
            
            Funcionario funcionario = banco.getFuncionarioCPF(cpf);
            var funcionarios = new ArrayList<Funcionario>();
            funcionarios.add(funcionario);
            mostrarFuncionarios(funcionarios);
                                    
        } catch(Exception e) {
            
            JOptionPane.showMessageDialog(
                    rootPane,
                    e.getMessage(),
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_bt_procurarCPFActionPerformed

