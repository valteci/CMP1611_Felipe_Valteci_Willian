    public void mostrarFuncionarios(ArrayList<Funcionario> funcionarios) {
        
        DefaultTableModel model = (DefaultTableModel) 
                                   jTable_consulta.getModel();
        
        while (model.getRowCount()> 0)
            model.removeRow(0);
        
        for (Funcionario funcionario : funcionarios) {
            
            if (funcionario instanceof Cozinheiro cozinheiro) {
                
                model.addRow(new Object[]{
                    cozinheiro.getCpf(),
                    cozinheiro.getNome(),
                    cozinheiro.getDataIngresso(),
                    cozinheiro.getSalario(),
                    cozinheiro.getNomeFantasia(),
                    "Cozinheiro"
                });
                
            } else if (funcionario instanceof Degustador){
                model.addRow(new Object[]{
                    funcionario.getCpf(),
                    funcionario.getNome(),
                    funcionario.getDataIngresso(),
                    funcionario.getSalario(),
                    "",
                    "Degustador"
                });
                
            } else {
                model.addRow(new Object[]{
                    funcionario.getCpf(),
                    funcionario.getNome(),
                    funcionario.getDataIngresso(),
                    funcionario.getSalario(),
                    "",
                    "Editor"
                });
            }
        }
    }
    
